interface RegionPickerProps {
  province: string;
  city: string;
  district: string;
  onChange: (province: string, city: string, district: string) => void;
}

export function RegionPicker({ province, city, district, onChange }: RegionPickerProps) {
  const provinces = Object.keys(regionData);
  const cities = province ? Object.keys(regionData[province] || {}) : [];
  const districts = province && city ? (regionData[province]?.[city] || []) : [];

  return (
    <div className="grid grid-cols-3 gap-2">
      <Select value={province} onValueChange={(v) => onChange(v, "", "")}>
        <SelectTrigger className="text-xs h-10">
          <SelectValue placeholder="省/直辖市" />
        </SelectTrigger>
        <SelectContent className="max-h-60">
          {provinces.map((p) => (
            <SelectItem key={p} value={p} className="text-xs">{p}</SelectItem>
          ))}
        </SelectContent>
      </Select>

      <Select value={city} onValueChange={(v) => onChange(province, v, "")} disabled={!province}>
        <SelectTrigger className="text-xs h-10">
          <SelectValue placeholder="市" />
        </SelectTrigger>
        <SelectContent className="max-h-60">
          {cities.map((c) => (
            <SelectItem key={c} value={c} className="text-xs">{c}</SelectItem>
          ))}
        </SelectContent>
      </Select>

      <Select value={district} onValueChange={(v) => onChange(province, city, v)} disabled={!city}>
        <SelectTrigger className="text-xs h-10">
          <SelectValue placeholder="区/县" />
        </SelectTrigger>
        <SelectContent className="max-h-60">
          {districts.map((d) => (
            <SelectItem key={d} value={d} className="text-xs">{d}</SelectItem>
          ))}
        </SelectContent>
      </Select>
    </div>
  );
}
