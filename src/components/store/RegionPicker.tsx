interface RegionPickerProps {
  province: string;
  city: string;
  district: string;
  onProvinceChange: (v: string) => void;
  onCityChange: (v: string) => void;
  onDistrictChange: (v: string) => void;
}

export function RegionPicker({ province, city, district, onProvinceChange, onCityChange, onDistrictChange }: RegionPickerProps) {
  const provinces = useMemo(() => Object.keys(REGION_DATA), []);
  const cities = useMemo(() => province ? Object.keys(REGION_DATA[province] || {}) : [], [province]);
  const districts = useMemo(() => (province && city) ? (REGION_DATA[province]?.[city] || []) : [], [province, city]);

  const handleProvinceChange = (v: string) => {
    onProvinceChange(v);
    onCityChange('');
    onDistrictChange('');
  };

  const handleCityChange = (v: string) => {
    onCityChange(v);
    onDistrictChange('');
  };

  return (
    <div className="space-y-2">
      <Label>所在地区 *</Label>
      <div className="grid grid-cols-3 gap-2">
        <Select value={province} onValueChange={handleProvinceChange}>
          <SelectTrigger className="text-xs h-9">
            <SelectValue placeholder="省份" />
          </SelectTrigger>
          <SelectContent className="max-h-60">
            {provinces.map(p => (
              <SelectItem key={p} value={p} className="text-xs">{p}</SelectItem>
            ))}
          </SelectContent>
        </Select>

        <Select value={city} onValueChange={handleCityChange} disabled={!province}>
          <SelectTrigger className="text-xs h-9">
            <SelectValue placeholder="城市" />
          </SelectTrigger>
          <SelectContent className="max-h-60">
            {cities.map(c => (
              <SelectItem key={c} value={c} className="text-xs">{c}</SelectItem>
            ))}
          </SelectContent>
        </Select>

        <Select value={district} onValueChange={onDistrictChange} disabled={!city}>
          <SelectTrigger className="text-xs h-9">
            <SelectValue placeholder="区/县" />
          </SelectTrigger>
          <SelectContent className="max-h-60">
            {districts.map(d => (
              <SelectItem key={d} value={d} className="text-xs">{d}</SelectItem>
            ))}
          </SelectContent>
        </Select>
      </div>
    </div>
  );
}
