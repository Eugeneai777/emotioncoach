export const REMINDERS_PER_CYCLE = 8;
export const TOTAL_STAGES = 4;

export const getEmotionById = (id: string): EmotionType | undefined => {
  return emotionTypes.find(e => e.id === id);
};

export const getStageIndex = (reminderIndex: number): number => {
  return Math.floor(reminderIndex / REMINDERS_PER_CYCLE);
};

export const getStageConfig = (emotion: EmotionType, reminderIndex: number) => {
  const stageIndex = getStageIndex(reminderIndex);
  return emotion.stages[stageIndex] || emotion.stages[0];
};
